// ----------------------------------------------------------------------
// Name: CNameValuePair
// Abstract: Hold a name and a value
// ----------------------------------------------------------------------
public class CNameValuePair
{
	
	// ----------------------------------------------------------------------
	// ----------------------------------------------------------------------
	// Constants
	// ----------------------------------------------------------------------
	// ----------------------------------------------------------------------


	// ----------------------------------------------------------------------
	// ----------------------------------------------------------------------
	// Properties( never make public )
	// ----------------------------------------------------------------------
	// ----------------------------------------------------------------------
	private int m_intValue = 0;
	private String m_strName = "";
	

	// ----------------------------------------------------------------------
	// ----------------------------------------------------------------------
	// Methods
	// ----------------------------------------------------------------------
	// ----------------------------------------------------------------------


	// ----------------------------------------------------------------------
	// Name: CNameValuePair
	// Abstract: Constructor
	// ----------------------------------------------------------------------
	public CNameValuePair( )
	{
		this( "", 0 );
	}
	

	// ----------------------------------------------------------------------
	// Name: CNameValuePair
	// Abstract: Constructor
	// ----------------------------------------------------------------------
	public CNameValuePair( String strName, int intValue )
	{
		m_strName = strName;
		m_intValue = intValue;
	}
	

	// ----------------------------------------------------------------------
	// Name: GetName
	// Abstract: Get the Name
	// ----------------------------------------------------------------------
	public String GetName( )
	{
		return m_strName;
	}

	// ----------------------------------------------------------------------
	// Name: GetValue
	// Abstract: Get the value
	// ----------------------------------------------------------------------
	public int GetValue( )
	{
		return m_intValue;
	}

	// ----------------------------------------------------------------------
	// Name: SetName
	// Abstract: Set the Name
	// ----------------------------------------------------------------------
	public void SetName( String strName )
	{
		m_strName = strName;
	}

	// ----------------------------------------------------------------------
	// Name: SetValue
	// Abstract: Set the value
	// ----------------------------------------------------------------------
	public void SetValue( int intValue )
	{
		m_intValue = intValue;
	}

	// ----------------------------------------------------------------------
	// Name: toString
	// Abstract: Get the Name
	// ----------------------------------------------------------------------
	public String toString( )
	{
		return m_strName;
	}

}

